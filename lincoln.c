// lincoln.c
#include <stdio.h>

// Function to print Abraham Lincoln's key accomplishments in Haiku format
void printLincolnHaiku() {
    printf("Sixteenth President,\n");
    printf("Led through Civil War, Union,\n");
    printf("Emancipation.\n\n");

    printf("Gettysburg Address,\n");
    printf("Redefined war's purpose, peace,\n");
    printf("Equality's voice.\n\n");

    printf("Thirteenth Amendment,\n");
    printf("Abolished slavery, free,\n");
    printf("Lincoln's legacy.\n");
}
